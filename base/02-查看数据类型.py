# 使用type内置类可以查看一个变量对应的数据类型
# int
a = 34
print(type(a))
# str
a = "hello"
print(type(a))
# bool
a = True
print(type(a))
# list
a = ['张三', '李四', '王五']
print(type(a))
# dict
a = {"name": "张三", "age":18, "address":"湖北"}
print(type(a))
# tuple
a = (1, 2, 3, 4)
print(type(a))
# set
a = {9, "hello", "world", 8, True, 8}
print(type(a))